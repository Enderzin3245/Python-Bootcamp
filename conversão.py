#inteiro para float
preço = 10
print(preço)

print(str(preço))

preço = float(preço)
print(preço)

#dividindo numeros inteiros resultará em um float
preço = 10 / 2
print(preço)

#uso de 2 barras preseva o resultado int
preço = 10 // 2
print(preço)

preço = 10.30
print(preço)

preço = int(preço)
print(preço)

